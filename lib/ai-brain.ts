export function decodeInstruction(input: string) {
  if (!input) return "";

  return `
AI CREATIVE DIRECTOR INTELLIGENCE:

AI understands user intent:
${input}

AI professionally decodes the request into production-grade cinematic direction.

Interpret:
- multilingual mixed language instructions
- emotional tone
- cinematic intent
- visual storytelling goal
- realism preference
- motion behavior if needed
`;
}
